public class Doctor{
    //instance variable
    private String name;
    
    //constructor
    public Doctor (String name){
        this.name = name;
    }
    
    //method to get doctor's name
    public String getName(){
        return name;
    }
    
    
    //method to set doctor's name
    public void setName(String doctor){
        name = doctor;
    }
}